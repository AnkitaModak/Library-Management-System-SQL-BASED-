/*Use %TYPE to declare:

Book Price
Member Name

Retrieve and display both values.*/
SET SERVEROUTPUT ON;

DECLARE
    VK_PRICE BOOKS.BOOKS_PRICE%TYPE;
    VK_MEMBERNAME MEMBERS.MEMBERNAME%TYPE;
BEGIN
    SELECT BOOKS_PRICE
    INTO VK_PRICE
    FROM BOOKS
    WHERE ROWNUM = 1;

    SELECT MEMBERNAME
    INTO VK_MEMBERNAME
    FROM MEMBERS
    WHERE ROWNUM = 1;

    DBMS_OUTPUT.PUT_LINE('Book Price: ' || VK_PRICE);
    DBMS_OUTPUT.PUT_LINE('Member Name: ' || VK_MEMBERNAME);
END;
/
